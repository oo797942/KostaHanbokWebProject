package ho.model;

public class HoBag {
	private String shopId;
	private String shopNo;
	private String shopSize;
	private String shopImg;
	private int shopSoo;
	private int shopPrice;
	private int shopSeqNo;
	
	

	public int getShopSeqNo() {
		return shopSeqNo;
	}

	public void setShopSeqNo(int shopSeqNo) {
		this.shopSeqNo = shopSeqNo;
	}

	public String getShopId() {
		return shopId;
	}

	public void setShopId(String shopId) {
		this.shopId = shopId;
	}

	public String getShopNo() {
		return shopNo;
	}

	public void setShopNo(String shopNo) {
		this.shopNo = shopNo;
	}

	public String getShopSize() {
		return shopSize;
	}

	public void setShopSize(String shopSize) {
		this.shopSize = shopSize;
	}

	public String getShopImg() {
		return shopImg;
	}

	public void setShopImg(String shopImg) {
		this.shopImg = shopImg;
	}

	public int getShopSoo() {
		return shopSoo;
	}

	public void setShopSoo(int shopSoo) {
		this.shopSoo = shopSoo;
	}

	public int getShopPrice() {
		return shopPrice;
	}

	public void setShopPrice(int shopPrice) {
		this.shopPrice = shopPrice;
	}

}
